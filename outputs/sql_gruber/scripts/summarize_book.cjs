const fs = require('fs');
const path = require('path');

const OUT_DIR = '.bookruns/egyptian_cross/stages/06_design/chapters_designed';
if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });

function saveAbsoluteChapter(id, title, content) {
    const fileName = `chapter${id}.html`;
    const html = `<!DOCTYPE html>
<html lang="ru">
<head>
    <meta charset="utf-8">
    <title>${title}</title>
    <link rel="stylesheet" href="assets/theme.css">
</head>
<body class="chapter-content">
    <div class="kicker">АРХИВЫ КУИНА: ДЕЛО №32</div>
    <h1>${title}</h1>
    ${content}
</body>
</html>`;
    fs.writeFileSync(path.join(OUT_DIR, fileName), html);
    console.log(`Absolute Chapter Saved: ${fileName}`);
}

